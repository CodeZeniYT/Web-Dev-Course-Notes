// 1️⃣ For Loop

// Example 1: Print numbers from 0 to 15
for (let i = 0; i <= 15; i++) {
  console.log(i);
}
console.log("For loop has ended");

// Example 2: Calculate sum of first 10 natural numbers
let sum = 0;
let n = 10;
for (let i = 1; i <= n; i++) {
  sum = sum + i;
}
console.log("Sum =", sum);
